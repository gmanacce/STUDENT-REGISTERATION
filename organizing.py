#####creating any list

birthday_months = ['january','february', 'june','september','november','december']

##### organize by using sort method

birthday_months.sort()

print(birthday_months)

#####using a reverse sort method

birthday_months = ['january','february', 'june','september','november','december']

birthday_months.sort(reverse = True)

print(birthday_months)

######using sorted method to temporary reverse list

birthday_months = ['january','february', 'match', 'april', 'may','june','july','august','september','october','november','december']

print(sorted(birthday_months))

print(birthday_months)

######using reverse method to a list

week_days = ['monday','tuesday','wednesday','thursday','friday','saturday','sunday']

print(week_days)

week_days.reverse()

